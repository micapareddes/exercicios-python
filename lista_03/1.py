num1 = float(input('Digite um número qualquer: '))

num2 = float(input('Digite outro número: '))

if num2 > num1:
    print(f'{num2} é maior do que {num1}')

if num2 == num1:
    print(f'Os números inseridos são iguais')

if num2 < num1: 
    print(f'O número {num2} é menor que o número {num1}')

